public class LineAndWidth {
    
    private String line;
    
    private int width;

    public LineAndWidth(String line, int width) {
        this.line = line;
        this.width = width;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
    
    
    
    
}
